#!/usr/bin/env node
//
// Generate supabase/migrations/002_seed_default_list.sql from
// data/restaurants.js so the seed stays reproducible. Run after editing
// the restaurants module:
//
//   node scripts/generate-default-list-sql.mjs
//
// The output migration is idempotent: it can be re-run safely and will
// upsert any changed columns. It intentionally raises if the owner
// account hasn't signed in yet -- create the auth.users row first by
// having kidhack@kidhack.com sign in once via magic link.

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const { RESTAURANTS } = await import(`file://${join(root, 'data/restaurants.js')}`);

if (!Array.isArray(RESTAURANTS) || RESTAURANTS.length === 0) {
  throw new Error('RESTAURANTS array is empty -- nothing to seed');
}
if (RESTAURANTS.length > 100) {
  throw new Error(`RESTAURANTS has ${RESTAURANTS.length} rows; cap is 100`);
}

const OWNER_EMAIL = 'kidhack@kidhack.com';
const OWNER_USERNAME = 'kidhack';
const SLUG = 'chronicle-top-100-2026';
const NAME = 'SF Chronicle Top 100 - 2026';

function sqlString(value) {
  if (value === null || value === undefined || value === '') return 'null';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlNumber(value) {
  if (value === null || value === undefined || Number.isNaN(value)) return 'null';
  return String(value);
}

const valueRows = RESTAURANTS
  .slice()
  .sort((a, b) => a.rank - b.rank)
  .map((r) => {
    const cells = [
      'new_list_id',
      sqlNumber(r.rank),
      sqlString(r.name),
      sqlString(r.address ?? null),
      sqlString(r.city ?? null),
      sqlString(r.cuisine ?? null),
      sqlString(r.url ?? null),
      sqlNumber(r.lat ?? null),
      sqlNumber(r.lng ?? null),
    ];
    return `      (${cells.join(', ')})`;
  })
  .join(',\n');

const sql = `-- ---------------------------------------------------------------------------
-- 002_seed_default_list.sql  (generated by scripts/generate-default-list-sql.mjs)
--
-- Seeds the SF Chronicle Top 100 as a real lists row owned by kidhack so
-- the rest of the app can treat it like any other custom list. Idempotent:
-- safe to re-run after editing data/restaurants.js (regenerate this file
-- by running the script).
--
-- Prerequisite: ${OWNER_EMAIL} must have signed in at least once so the
-- corresponding auth.users row exists. The DO block raises if it doesn't.
-- ---------------------------------------------------------------------------

do $$
declare
  owner       uuid;
  new_list_id uuid;
begin
  select id into owner from auth.users where email = '${OWNER_EMAIL}';
  if owner is null then
    raise exception
      '${OWNER_EMAIL} has not signed in yet -- have them sign in, then rerun this migration';
  end if;

  insert into public.profiles (user_id, username)
  values (owner, '${OWNER_USERNAME}')
  on conflict (user_id) do nothing;

  insert into public.lists (owner_id, slug, name)
  values (owner, '${SLUG}', '${NAME.replace(/'/g, "''")}')
  on conflict (slug) do update set
    name       = excluded.name,
    updated_at = now()
  returning id into new_list_id;

  insert into public.list_items (list_id, rank, name, address, city, cuisine, url, lat, lng) values
${valueRows}
  on conflict (list_id, rank) do update set
    name    = excluded.name,
    address = excluded.address,
    city    = excluded.city,
    cuisine = excluded.cuisine,
    url     = excluded.url,
    lat     = excluded.lat,
    lng     = excluded.lng;
end $$;
`;

const outPath = join(root, 'supabase/migrations/002_seed_default_list.sql');
await mkdir(dirname(outPath), { recursive: true });
await writeFile(outPath, sql);
console.log(`Wrote ${outPath} (${RESTAURANTS.length} items)`);
